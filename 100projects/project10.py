def add(n1,n2):
    return n1+n2

def sub(n1,n2):
    return n1-n2

def mul(n1,n2):
    return n1*n2

def div(n1,n2):
    return n1/n2

a=int(input("Enter the first number : "))

operations={
    "+" : add,
    "-" : sub,
    "*" : mul,
    "/" : div
}

for symbol in operations:
    print(symbol)

flag="y"
while flag=="y":
    operation_symbol = input("Pick an operation : ")

    b=int(input("What's next number : "))

    operation_function=operations[operation_symbol]
    answer=operation_function(a,b)

    print(f"{a} {operation_symbol} {b} = {answer}")
    a=answer
    flag=input("Type 'y' for continue , or type 'n' to exit : ").lower()
   